package Employes;

import java.util.Date;

public class Employees {
    private String name;
    private Date dob;
    private double salary;
    private double tax;

    public void setName(String name) {
        this.name = name;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public void setTax(double tax) {
        this.tax = tax;
    }
}
